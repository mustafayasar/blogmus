        $model = new ContactForm();

        if ($model->load(Yii::$app->request->post()) && $model->validate()) {

            if ($model->sendEmail('mustafayasar@live.com')) {
                Yii::$app->session->addFlash('success', 'Mesaj覺n覺z iletildi.');
            } else {
                Yii::$app->session->addFlash('error', 'HATA! Mesaj覺n覺z iletilemedi.');
            }

            return $this->refresh();
        }

        return $this->render('contact', [
            'model' => $model,
        ]);
    }
